movies = ["1", "2", "3"]
print("==========for each_flick in movies===========")
for each_flick in movies:
    print(each_flick)
print("==========for each_item in movies===========")
for each_item in movies:
    print(each_item)
print("==========while count < len(movies)===========")
count=0
while count < len(movies):
    print(movies[count])
    count=count+1
